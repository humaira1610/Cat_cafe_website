        <h2>What is a cat cafe?</h2>
        <p>A cat cafe is essentially a coffee shop with free roaming cats. 
        The idea of cat cafes originated in Taiwan in 1988, and was brought to Japan in around 2005.
        In East Asia, most people live in apartments and are not allowed to have pets, so the cats in the cat cafes there tend to be permanent residents.
        Cat cafes have now spread throughout the world. Korea, Austria, Spain, Germany are among the many countries where cat cafes can be found.</p>
        <p><h3>Why do people visit cat cafes?</h3></p>
        <p>Due to restrictions regarding owning pets, or maybe someone in the family being allergic to cats, many cat lovers are not able to have pet cats.
        Cat cafes are a way these people can spend time with cats and interact with them while enjoying snacks and some coffee to relieve stress after a hectic day.<p>
        
